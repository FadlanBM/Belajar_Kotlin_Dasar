fun main() {
    //bilangan bulat
    var umur:Long=304343434343L

    //bilangan pecahan
    var tinggi:Double=172.3
    var beratBadan:Float=28.2F

    //Literals
    var decimal: Int=100
    var hexadecimal: Int=0xFF
    var binary: Int=0b000111

    //ubderscore in number
    var harga: Int=10_000_000

    //Output
    println(umur)
    println(tinggi)
    println(decimal)
    println(hexadecimal)
    println(binary)
    println(harga)
    println(beratBadan)

}