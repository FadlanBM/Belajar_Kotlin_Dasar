fun main() {
    // Perbandingan antara bilangan

    var a=10;
    var b=10;
    // perbandinan (=)
    var perbandingan=a==b
    println("Hasil perbandingan adalah " + perbandingan)

    a=10;
    b=11;
    // Perbandingan (<)
    perbandingan=a<b
    println("Hasil perbandingan adalah " + perbandingan)

    a=10;
    b=11;
    // Perbandingan (>)
    perbandingan=b>a
    println("Hasil perbandingan adalah " + perbandingan)

    a=10;
    b=11;
    // Perbandingan (<=)
    perbandingan=a<=b
    println("Hasil perbandingan adalah " + perbandingan)

    a=10;
    b=11;
    // Perbandingan (>=)
    perbandingan=b>=a
    println("Hasil perbandingan adalah " + perbandingan)

    a=10;
    b=11;
    // Perbandingan (!=)
    perbandingan=a!=b
    println("Hasil perbandingan adalah " + perbandingan)

    //Perbandingan antara Karakter

    //perbandinan (==)
    print("Hasil perbandingan adalah ")
    println("a" == "a")

    //perbandingan (<)
    print("Hasil perbandingan adalah ")
    println("a" < "b")

    //perbandingan (>)
    print("Hasil perbandingan adalah ")
    println("d" > "c")

    //Perbandingan (<=)
    print("Hasil perbandingan adalah ")
    println("b" <= "b")

    //Perbandingan (>=)
    print("Hasil perbandingan adalah ")
    println("c">="c")

    //Perbandingan (!=)
    print("Hasil perbandingan adalah ")
    println("a" != "b")
}